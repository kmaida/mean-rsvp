(function() {
	'use strict';

	angular
		.module('myApp')
		.directive('navControl', navControl);

	navControl.$inject = ['mediaCheck', 'MQ', '$timeout'];

	/**
	 * function navControl()
	 *
	 * @param mediaCheck
	 * @param MQ
	 * @param $timeout
	 * @returns {{restrict: string, link: navControlLink}}
	 */
	function navControl(mediaCheck, MQ, $timeout) {

		navControlLink.$inject = ['$scope', '$element', '$attrs'];

		/**
		 * function navControlLink()
		 *
		 * @param $scope
		 * @param $element
		 * @param $attrs
		 *
		 * navControl directive link function
		 */
		function navControlLink($scope, $element, $attrs) {
			var body = angular.element('body'),
				openNav = function () {
					body
						.removeClass('nav-closed')
						.addClass('nav-open');

					$scope.navOpen = true;
				},
				closeNav = function () {
					body
						.removeClass('nav-open')
						.addClass('nav-closed');

					$scope.navOpen = false;
				};

			mediaCheck.init({
				scope: $scope,
				mq: MQ.SMALL,
				enter: function () {
					closeNav();

					$timeout(function () {
						$scope.toggleNav = function () {
							if (body.hasClass('nav-closed')) {
								openNav();
							} else {
								closeNav();
							}
						};
					});

					$scope.$on('$locationChangeSuccess', closeNav);
				},
				exit: function () {
					$timeout(function () {
						$scope.toggleNav = null;
					});

					body.removeClass('nav-closed nav-open');
				}
			});
		}

		return {
			restrict: 'EA',
			link: navControlLink
		};
	}

})();